// para la animacion de la ruedecita
void loadingAnimation(std::string label, int cycles, int delayMs) 
{
    const char spinner[] = "|/-\\";

    for (int i = 0; i < cycles; ++i) 
    {
        std::cout << "\r" << label << " " << spinner[i % 4] << std::flush;
        std::this_thread::sleep_for(std::chrono::milliseconds(delayMs));//hace una pausa
    }
    std::cout << "\r" << label << " Hecho!     \n";
}

